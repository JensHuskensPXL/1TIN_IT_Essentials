kleinste_getal = float(input("geef het kleinste getal in: "))
grootste_getal = float(input("geef het grootste getal in: "))

print("Het kleinste getal is: ", kleinste_getal)

kwadraat_kleinste_getal = kleinste_getal ** 2

print("Het kwadraat van het kleinste getal is: ", kwadraat_kleinste_getal)

deling = grootste_getal / kleinste_getal

print("De deling van het grootste getal gedeeld door het kleinste is: ", deling)