# Fahrenheit in en de computer berekent het aantal graden Celsius,
#  afgerond op 1 decimaal na de komma. Dit aantal wordt afgedrukt.

aantal_graden_farenheit = float(input("Geef het aantal graden farenheit in: "))

farenheit = 5 / 9 * (aantal_graden_farenheit - 32)
print("aantal graden celsius", farenheit)



